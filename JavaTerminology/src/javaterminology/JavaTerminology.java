package javaterminology;

/**
 * this class displays 10 terms used with java and their definitions
 */
public class JavaTerminology {

    public static void main(String[] args) {
        System.out.println("Java Terminology\n"); // chapter 03 start
        System.out.println("Bytecode:\nCompiled Java source code.\n");
        System.out.println("Controlling Class:\nThe class in an application that contains the main() method.\n");
        System.out.println("Java Virtual Machine (Java VM):\nThe Java bytecode interpreter.\n");
        System.out.println("Just-in-time compiler (JIT):\nSoftware that converts bytecode to specific machine code.\n");
        System.out.println("Object-oriented programming (OOP):\nA systematic, modular approach to developing complex programs.\n");
        System.out.println("ArithmeticException exception:\nAn exception thrown when division by 0 occurs.\n"); // chapter 04 start
        System.out.println("InputMismatchException exception:\nAn exception thrown when user input is not as expected.\n");
        System.out.println("Instantiation:\nTo create a new object.\n");
        System.out.println("Literal:\nAn actual value.\n");
        System.out.println("Type Casting:\nConverting a number from one type to a different type.");
    }

}